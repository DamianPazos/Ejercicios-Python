# Solicita al usuario dos números y después imprime su suma.

# Declaro las variables
numero_uno = int(input("Ingrese el primer numero: "))
numero_dos = int(input("Ingrese el segundo numero: "))

# Realizo la suma e imprimo en pantalla
print(numero_uno + numero_dos) # El operados '+' suma los valores ya que son de tipo 'int'. Si fuesen de tipo 'string' generaria una concatenacion.