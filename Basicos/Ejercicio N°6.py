# Solicita al usuario dos números y después imprime su división.

# Declaracion de variables
numero_uno = int(input("Ingresa el primer numero: "))
numero_dos = int(input("Ingresa el segundo numero (debe ser distinto de 0): "))

# Divido e imprimo en pantalla
print(numero_uno / numero_dos) # El operador '/' sirve para dividir numeros